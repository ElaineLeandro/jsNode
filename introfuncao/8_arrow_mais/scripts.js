/* Arrow Fuctions
  -> Se só um paramentro podemos resolver parentes do argumento e o return
  -> Se a expressão for pequena, pode até ser feita em uma linha sem prejudicar a leitura do código
* */

const raizQuadrada = (x) => {
  return x * x
}

// const raizQuadrada = n => n * n;

console.log(raizQuadrada(2))
console.log(raizQuadrada(4))

const multiplicarPorDois = (a) =>{
  return a * 2
}

console.log(multiplicarPorDois(2))

const multiplicarPortres = b => b * 3

console.log(multiplicarPortres(5))