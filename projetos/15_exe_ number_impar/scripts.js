//Escrever um loop for ou while que exibe qual númber é par e qual número é impar,
//O contador deve iniciar em 0 e ir até 50;

for(number=0;number < 50; number ++){
   if(number % 2 == 0){
      console.log(`O numero é ${number}  par`)
}else{
   console.log(`O numero é ${number}  impar`)
}
   
}



