/**Função built-in:Math.x()
 * Função utilizada para expressões/cálculos matemáticos
 * Ex: let maiorNumero = Math.max(1,4,3,6)
 * console.log(maiorNumero)
 * Esta função é muito utilizada.
 * 
*/

let maiorNumero = Math.max(1,4,3,6)
console.log(maiorNumero)

let manorNumero = Math.min(12,16,22,37)
console.log(manorNumero)

let arrendodar = Math.round(5.2934723)
console.log(arrendodar)

let arrendodarParaCima = Math.ceil(5.2934723)
console.log(arrendodarParaCima)