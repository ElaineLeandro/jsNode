/** Booleans: comparações
 * Maior que: >
 * Menor que: <
 * Maior ou igual: >=
 * Menor ou igual: <=
 * Igual: ==
 * Diferente: !=
 * idêndico: ===
  */

console.log(1 > 2);
console.log( 5 < 10);
console.log( 3>= 3);
console.log( 5 <= 4);
console.log( 'Elaine' != 'Elaine');
console.log( 8 === '8');
console.log( 4 === 4);

