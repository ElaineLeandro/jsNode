/** Booleans: Operadores Logiscos
 * Por meio de uma comparação resultam em um Boolean
 * && - and -> para ser true, os dois 'lados' da comparação precisam ser true
 * || - or -> para ser true, basta um dos 'lados da comparação precisam ser true;
 * ! - not -> inverte os valores (true vira false)
  */


 console.log(true && true)
// console.log(true && false)
 console.log(false || false)
// console.log(false || false)


console.log( 5 > 3 && 3 == 2)
console.log( 5 > 3 || 3 == 1)

console.log( 8 == 8 && "Bento" == "Bento")
console.log("Benicio" == "Beni" || false)

console.log( !(true && true))

